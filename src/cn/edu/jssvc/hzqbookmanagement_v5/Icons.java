package cn.edu.jssvc.hzqbookmanagement_v5;

public class Icons {

	private String name;

	private int imageId;

	public Icons(String name, int imageId) {
		this.name = name;
		this.imageId = imageId;
	}

	public String getName() {
		return name;
	}

	public int getImageId() {
		return imageId;
	}

}
